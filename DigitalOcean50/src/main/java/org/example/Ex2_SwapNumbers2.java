package org.example;

public class Ex2_SwapNumbers2 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        b = b - a ; // 20 - 10 = 10 , b=10
        a = a + b; // 10 + 10(b) =20 a =20
        //output is a=20, b =10
        System.out.println("a = " + a);
        System.out.println("b =" + b);


    }
}
